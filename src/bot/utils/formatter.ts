/**
 * Экранирует спецсимволы HTML для безопасной отправки в Telegram с parse_mode: HTML.
 */
export function escapeHtml(text: any): string {
    if (text === null || text === undefined) return '';
    const str = String(text);
    return str
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#039;');
}


